// The smart contract address of the crowdsale contract on the BSC testnet.
const contractAddress = process.env.CONTRACT_ADDRESS;
// Your account address on the BSC testnet.
const accountAddress = process.env.ACCOUNT_ADDRESS;



/**
 * Function to prepare a transaction to buy tokens from the crowdsale contract.
 * @param {number} weiAmount The amount in wei to send with the token purchase transaction.
 */
const prepareBuyTokensTransaction = async (weiAmount) => {
    const web3 = new Web3(RPC);
    const crowdsaleContract = new web3.eth.Contract(TokenCrowdsaleABI, contractAddress);

    // Preparing the transaction data to buy tokens.
    const tx = crowdsaleContract.methods.buyTokens(accountAddress);
    const gas = await tx.estimateGas({ from: accountAddress });
    const gasPrice = await web3.eth.getGasPrice();
    const data = tx.encodeABI();
    const nonce = await web3.eth.getTransactionCount(accountAddress);

    console.log({
        from: accountAddress,
        to: crowdsaleContract.options.address,
        value: weiAmount,
        data: data,
        gas: gas,
        gasPrice: gasPrice,
        nonce: nonce,
    });
	// The transaction data is ready to be sent to the BSC testnet.
	return {
		from: accountAddress,
		to: crowdsaleContract.options.address,
		value: weiAmount,
		data: data,
		gas: gas,
		gasPrice: gasPrice,
		nonce: nonce,
	};
};

prepareBuyTokensTransaction(Web3.utils.toWei("1", 'ether'));

